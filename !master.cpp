bool dfs(int u,int pe) {
	vis[u]=1;
	for (auto [v,w]:e[u]) {
		if (!vis[v]) {
			pa[v]=u;
			if (dfs(v,w)) return true;
		} else if (w!=pe&&vis[v]) {
			int x=u;
			while (x!=v) {
				cyc.pb(x);
				x=pa[x];
			}
			cyc.pb(v);
			return true;
		}
	}
	return false;
}

